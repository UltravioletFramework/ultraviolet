
namespace Ultraviolet.Presentation
{
    /// <summary>
    /// Represents a value which specifies how to align a block of text.
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// The text is left-aligned.
        /// </summary>
        Left,

        /// <summary>
        /// The text is right-aligned.
        /// </summary>
        Right,

        /// <summary>
        /// The text is centered.
        /// </summary>
        Center,
    }
}
