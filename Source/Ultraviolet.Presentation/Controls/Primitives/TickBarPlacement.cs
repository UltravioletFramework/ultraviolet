namespace Ultraviolet.Presentation.Controls.Primitives
{
    /// <summary>
    /// Represents the placement of a <see cref="TickBar"/> element.
    /// </summary>
    public enum TickBarPlacement
    {
        /// <summary>
        /// The <see cref="TickBar"/> is to the left of its <see cref="Slider"/> parent.
        /// </summary>
        Left,

        /// <summary>
        /// The <see cref="TickBar"/> is above its <see cref="Slider"/> parent.
        /// </summary>
        Top,

        /// <summary>
        /// The <see cref="TickBar"/> is to the right of its <see cref="Slider"/> parent.
        /// </summary>
        Right,

        /// <summary>
        /// The <see cref="TickBar"/> is below its <see cref="Slider"/> parent.
        /// </summary>
        Bottom,
    }
}
