
namespace Ultraviolet.Presentation.Controls
{
    /// <summary>
    /// Represents the position of a child element within a <see cref="DockPanel"/>.
    /// </summary>
    public enum Dock
    {
        /// <summary>
        /// The child is docked to the left edge of the panel.
        /// </summary>
        Left,

        /// <summary>
        /// The child is docked to the top edge of the panel.
        /// </summary>
        Top,

        /// <summary>
        /// The child is docked to the right edge of the panel.
        /// </summary>
        Right,

        /// <summary>
        /// The child is docked to the bottom edge of the panel.
        /// </summary>
        Bottom,
    }
}
