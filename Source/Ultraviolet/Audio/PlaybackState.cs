
namespace Ultraviolet.Audio
{
    /// <summary>
    /// Represents the playback state of a song or sound effect.
    /// </summary>
    public enum PlaybackState
    {
        /// <summary>
        /// Playback is stopped.
        /// </summary>
        Stopped,

        /// <summary>
        /// Playback is in progress.
        /// </summary>
        Playing,

        /// <summary>
        /// Playback is paused.
        /// </summary>
        Paused,
    }
}
