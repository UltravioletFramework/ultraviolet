
namespace Ultraviolet.Platform
{
    /// <summary>
    /// Represents a window's maximization or minimization state.
    /// </summary>
    public enum WindowState
    {
        /// <summary>
        /// The window is in a normal state.
        /// </summary>
        Normal,

        /// <summary>
        /// The window is minimized.
        /// </summary>
        Minimized,

        /// <summary>
        /// The window is maximized.
        /// </summary>
        Maximized,
    }
}
