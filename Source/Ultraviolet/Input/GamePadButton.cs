
namespace Ultraviolet.Input
{
    /// <summary>
    /// Represents the buttons available on a <see cref="GamePadDevice"/>.
    /// </summary>
    public enum GamePadButton
    {
        /// <summary>
        /// No game pad button.
        /// </summary>
        None,

        /// <summary>
        /// The A button.
        /// </summary>
        A,

        /// <summary>
        /// The B button.
        /// </summary>
        B,

        /// <summary>
        /// The X button.
        /// </summary>
        X,

        /// <summary>
        /// The Y button.
        /// </summary>
        Y,
        
        /// <summary>
        /// The Back button.
        /// </summary>
        Back,

        /// <summary>
        /// The Guide button.
        /// </summary>
        Guide,

        /// <summary>
        /// The Start button.
        /// </summary>
        Start,

        /// <summary>
        /// The left joystick button.
        /// </summary>
        LeftStick,

        /// <summary>
        /// The right joystick button.
        /// </summary>
        RightStick,

        /// <summary>
        /// The left shoulder button.
        /// </summary>
        LeftShoulder,

        /// <summary>
        /// The right shoulder button.
        /// </summary>
        RightShoulder,

        /// <summary>
        /// The up button on the directional pad.
        /// </summary>
        DPadUp,

        /// <summary>
        /// The down button on the directional pad.
        /// </summary>
        DPadDown,

        /// <summary>
        /// The left button on the directional pad.
        /// </summary>
        DPadLeft,

        /// <summary>
        /// The right button on the directional pad.
        /// </summary>
        DPadRight,

        /// <summary>
        /// The left stick when it is pressed up.
        /// </summary>
        LeftStickUp,

        /// <summary>
        /// The left stick when it is pressed down.
        /// </summary>
        LeftStickDown,

        /// <summary>
        /// The left stick when it is pressed left.
        /// </summary>
        LeftStickLeft,

        /// <summary>
        /// The left stick when it is pressed right.
        /// </summary>
        LeftStickRight,

        /// <summary>
        /// The right stick when it is pressed up.
        /// </summary>
        RightStickUp,

        /// <summary>
        /// The right stick when it is pressed down.
        /// </summary>
        RightStickDown,

        /// <summary>
        /// The right stick when it is pressed left.
        /// </summary>
        RightStickLeft,

        /// <summary>
        /// The right stick when it is pressed right.
        /// </summary>
        RightStickRight,

        /// <summary>
        /// The left trigger when it is fully depressed.
        /// </summary>
        LeftTrigger,

        /// <summary>
        /// The right trigger when it is fully depressed.
        /// </summary>
        RightTrigger,
    }
}
