
namespace Ultraviolet.Input
{
    /// <summary>
    /// Represents the axes available on a <see cref="GamePadDevice"/>.
    /// </summary>
    public enum GamePadAxis
    {
        /// <summary>
        /// No game pad axis.
        /// </summary>
        None,

        /// <summary>
        /// The left joystick x-axis.
        /// </summary>
        LeftJoystickX,

        /// <summary>
        /// The left joystick y-axis.
        /// </summary>
        LeftJoystickY,

        /// <summary>
        /// The right joystick x-axis.
        /// </summary>
        RightJoystickX,

        /// <summary>
        /// The right joystick y-axis.
        /// </summary>
        RightJoystickY,

        /// <summary>
        /// The left trigger axis.
        /// </summary>
        LeftTrigger,

        /// <summary>
        /// The right trigger axis.
        /// </summary>
        RightTrigger,
    }
}
