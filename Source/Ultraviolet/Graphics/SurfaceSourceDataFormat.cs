namespace Ultraviolet.Graphics
{
    /// <summary>
    /// Represents the formats in which a <see cref="SurfaceSource"/> can store its data.
    /// </summary>
    public enum SurfaceSourceDataFormat
    {
        /// <summary>
        /// The surface source's data is stored in RGBA order.
        /// </summary>
        RGBA,

        /// <summary>
        /// The surface source's data is stored in BGRA order.
        /// </summary>
        BGRA,
    }
}
