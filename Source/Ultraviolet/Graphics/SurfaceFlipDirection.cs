namespace Ultraviolet.Graphics
{
    /// <summary>
    /// Represents the directions in which a surface can be flipped.
    /// </summary>
    public enum SurfaceFlipDirection
    {
        /// <summary>
        /// Doesn't flip the surface.
        /// </summary>
        None,

        /// <summary>
        /// Flips the surface horizontally.
        /// </summary>
        Horizontal,

        /// <summary>
        /// Flips the surface vertically.
        /// </summary>
        Vertical,
    }
}
