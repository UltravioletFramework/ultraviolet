
namespace Ultraviolet.Graphics
{
    /// <summary>
    /// Represents the supported texture filtering mode.
    /// </summary>
    public enum TextureFilter
    {
        /// <summary>
        /// Use point filtering.
        /// </summary>
        Point,

        /// <summary>
        /// Use linear filtering.
        /// </summary>
        Linear,

        /// <summary>
        /// Use anisotropic filtering.
        /// </summary>
        Anisotropic,
    }
}
