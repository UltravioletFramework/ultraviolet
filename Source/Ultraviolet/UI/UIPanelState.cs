
namespace Ultraviolet.UI
{
    /// <summary>
    /// Represents a UI panel's possible transition states.
    /// </summary>
    public enum UIPanelState
    {
        /// <summary>
        /// The panel is in the process of opening.
        /// </summary>
        Opening,

        /// <summary>
        /// The panel is open.
        /// </summary>
        Open,

        /// <summary>
        /// The panel is in the process of closing.
        /// </summary>
        Closing,

        /// <summary>
        /// The panel is closed.
        /// </summary>
        Closed,
    }
}
